#ifndef LLVM_LIB_TARGET_MCS6502_INSTPRINTER_MC6502INSTPRINTER_H
#define LLVM_LIB_TARGET_MCS6502_INSTPRINTER_MC6502INSTPRINTER_H

#include "MCTargetDesc/MCS6502MCTargetDesc.h"
#include "llvm/MC/MCInstPrinter.h"

namespace llvm {

class MCOperand;

class MCS6502InstPrinter : public MCInstPrinter {
public:
  MCS6502InstPrinter(const MCAsmInfo &MAI, const MCInstrInfo &MII,
                     const MCRegisterInfo &MRI)
      : MCInstPrinter(MAI, MII, MRI) {}

  void printInst(const MCInst *MI, raw_ostream &O, StringRef Annot,
                 const MCSubtargetInfo &STI) override;

  void printRegName(raw_ostream &O, unsigned RegNo) const override;

  void printOperand(const MCInst *MI, unsigned OpNo, raw_ostream &O,
                    const char *Modifier = nullptr);
  void printImmediate(const MCInst *MI, unsigned OpNo, raw_ostream &O,
                      const char *Modifier = nullptr);
  void printImmediate16(const MCInst *MI, unsigned OpNo, raw_ostream &O,
                        const char *Modifier = nullptr);

  // Autogenerated by tblgen
  void printInstruction(const MCInst *MI, raw_ostream &O);

  static const char *getRegisterName(unsigned RegNo);
};

} // namespace llvm

#endif // LLVM_LIB_TARGET_MCS6502_INSTPRINTER_MC6502INSTPRINTER_H
